#include <stdlib.h>
#include <stdio.h>

/**
 * print_alphabet -alphabets in lowercase
 * Description: This program prints the alphabet in lowercase
 * Return: Always (0) success
 */

int print_alphabet(void)
{
	char c;

	for (c = 'a'; c <= 'z'; c++)
	putchar (c);
	{
	putchar('\n');
	}
	return (0);
}
